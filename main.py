# Получаем данные от пользователя
data = input("Введите элементы списка через пробел: ").split()

# Преобразуем список (можно добавить int(), если нужны числа)

# Способ 1: Использование множества set()
unique_elements = set(data)

# Выводим результат
print("Количество уникальных элементов:", len(unique_elements))
print("Уникальные элементы:", unique_elements)
